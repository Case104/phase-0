def add(num_1, num_2)
	sum = num_1+num_2
	return sum
end

def subtract(num_1, num_2)
	difference = num_1+-num_2
	return difference
end

def multiply(num_1, num_2)
	product = num_1*num_2
	return product
end

def divide(num_1, num_2)
	quotient = num_1.to_f / num_2.to_f
	return quotient
end
